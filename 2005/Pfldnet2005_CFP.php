<div class="Section1">   
<p class="MsoNormal" align="center" style="text-align: center;"><big><big><big><big><big><big><font
 face="Times New Roman, Times, serif"><b><span lang="EN-GB"
 style="font-size: 10pt; font-family: &quot;Courier New&quot;;">Call For Papers (<a href="Pfldnet2005_CFP.txt">text version</a>)<br>
  ===============================</span><span lang="EN-GB" style=""><o:p></o:p></span></b></font></big></big></big></big></big></big></p>
    
<p class="MsoNormal" align="center" style="text-align: center;"><big><big><big><big><big><big><font
 face="Times New Roman, Times, serif"><b><span lang="EN-GB"
 style="font-size: 10pt; font-family: &quot;Courier New&quot;;">Third International 
Workshop on Protocols for Fast Long-Distance Networks<o:p></o:p></span></b></font></big></big></big></big></big></big></p>
    
<p class="MsoNormal" align="center" style="text-align: center;"><big><big><font
 face="Times New Roman, Times, serif"><b><span lang="EN-GB"
 style="font-size: 10pt; font-family: &quot;Courier New&quot;;"><big><big><big><big>Pfldnet2005</big></big></big></big><o:p></o:p></span></b></font></big></big></p>
    
<p class="MsoNormal" align="center" style="text-align: center;"><big><font
 face="Times New Roman, Times, serif"><b><span lang="EN-GB"
 style="font-size: 10pt; font-family: &quot;Courier New&quot;;">-------------------------------------------------------------------</span></b></font><br>
 </big></p>
 
<p class="MsoNormal" align="center" style="text-align: center;"><big><span
 lang="EN-GB" style=""><o:p></o:p></span></big></p>
    
<p class="MsoNormal"><big><span lang="EN-GB" style="font-size: 10pt;">
 </span></big></p>
 
<div align="center"><span lang="EN-GB" style="font-size: 10pt;"><big><a
 href="http://www.ens-lyon.fr/LIP/RESO/pfldnet2005">http://www.ens-lyon.fr/LIP/RESO/pfldnet2005</a>
 <br><a href="mailto:pfldnet2005@ens-lyon.fr">pfldnet2005@ens-lyon.fr</a>
 </big>
  </span></div>
    
<p><span lang="EN-GB" style="">Fast long-distance networks (i.e., networks 
operating at 622 Mbit/s, 2.5 Gbit/s, or 10 Gbit/s and spanning several countries 
or states) are now becoming commonplace. More and more researchers now routinely 
transfer between 10 GB and multi-TB datasets over gigabit networks. Application 
domains for such massive transfers include data-intensive Grids (e.g., in 
Particle Physics, Earth Observation, Bio informatics, and Radio Astronomy), 
database mirroring for Web sites (e.g., in e-commerce), and push-based Web 
cache updates.<o:p></o:p></span></p>
    
<p><span lang="EN-GB" style="">Although the network infrastructure is now 
in place, or will soon be, the transport and application protocols available 
to date perform rather poorly over such networks. Current versions of TCP, 
for instance, recover very slowly from packet loss when the RTT and the link 
capacity are large, thus requiring a large congestion window for high throughput. 
A number of research teams have begun investigating these protocol issues. 
<o:p></o:p></span></p>
    
<p><span lang="EN-GB" style="">The </span><a
 href="http://datatag.web.cern.ch/datatag/pfldnet2003/"><span
 lang="EN-GB" style="">First International Workshop on Protocols for Fast 
Long-Distance Networks </span></a><span lang="EN-GB" style="">and the </span><a
 href="http://www-didc.lbl.gov/PFLDnet2004/"><span lang="EN-GB" style="">Second 
International Workshop on Protocols for Fast Long-Distance Networks </span></a><span
 lang="EN-GB" style="">were very successful in bringing together many researchers 
from the U.S., Asia and Europe who are working on these problems. This workshop 
will continue this tradition, and provide a perfect setting for researchers 
in this area to exchange ideas and experience.<o:p></o:p></span></p>
    
<p class="MsoNormal"><span lang="EN-GB" style="">This single-track workshop 
will provide researchers and technologists with a focused, highly interactive 
opportunity to present, discuss and exchange experience on leading research, 
development and future directions in high performance transport and application 
protocols (TCP, UDP, HTTP, FTP, etc.) over fast long-distance networks. <o:p></o:p></span></p>
<br>    
<p class="MsoNormal"><span lang="EN-GB" style="">This year, a particular emphasis
will be on End Systems Issues (hardware and software). Each day will start
with a plenary talk and end with a panel. In between, formal presentations 
of papers will be followed by extensive and informal Q&amp;A sessions. </span><span
 lang="EN-GB" style="">Authors are invited to <br>
  submit unpublished extended abstract for consideration</span><span
 lang="EN-GB" style="font-size: 10pt; font-family: &quot;Courier New&quot;;">.<o:p></o:p></span></p>
    
<p><span lang="EN-GB" style="">In order to facilitate discussions, attendance 
will be limited to 60 participants. Please register early to ensure your participation.
Depending on the number of people who register, we may need to restrict the
number of people from a given organization to allow for a broader representation
of the research community. Registration will open on October 1, 2004. <o:p></o:p></span></p>
    
<p><u><strong><span lang="EN-GB" style="">Call For Papers</span></strong></u><span
 lang="EN-GB" style=""><o:p></o:p></span></p>
    
<p><span lang="EN-GB" style="">
   Participants wishing to present a paper should upload a four-pages extended 
 abstract to <a href="Soumission">the submission web page</a> by October 15 (2004). 
 Authors whose abstracts are selected for presentation will have 
the option to submit a full paper, to be published on the PFLDnet 2005 web 
site and in the PFLDnet 2005 proceedings. We may try to collect the best papers
for a special edition of a journal, if the authors are interested.<o:p></o:p></span></p>
    
<p class="MsoNormal"><u><span lang="EN-GB" style="font-size: 10pt;">
