        <li>Transport service for Grids</li>
        <li>QoS and scalability issues</li>
        <li>Multicast over fast long-distance networks</li>
        </ul>
  </li>
  </ul>
</dl>

<p>Authors whose papers are selected for presentation will have the option to submit a revised paper,
to be published on the PFLDnet 2007 web site and in the PFLDnet 2007 proceedings.</p>
