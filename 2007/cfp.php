<p>Future networks will increasingly contain high bandwidth, high delay paths.  Protocols for fast, long distance networks will have to expand from experimental testbeds and into the wider world. In that process, it is increasingly important for protocols which achieve high utilization on fast, long distance networks also to have
the qualities required for widespread use, such as fairness and scalability to many users.</p>
<p>PFLDnet2007 solicits papers which will further research towards on protocols  for tomorrow's high speed internet, at all stages along the continuum from the requirements of today's grid networks, through benchmarking of proposals, to the final selection of the next generation of TCP.</p>
<p>Participants wishing to present a paper should upload a full paper to the submission site by <span  style="text-decoration: line-through">Friday 13 October, 2006 Friday 20 October , 2006 (Extended)</span>.<br>
Papers should be typically 4-5 pages, but no longer than 6 pages.</p>
<dl>
<dt>Scope:<SPAN class="medium"> (choose at least one of the subject areas below - multiple selections possible)</SPAN></dt>
<ul>
  <li>Protocol issues in fast long-distance networks</li>
  <li>Protocol development
    <ul>
      <li>Enhancements of TCP and its variants</li>
      <li>Novel data transport protocols designed for new application services</li>
      <li>Explicit signaling protocols: Optimization criteria and deployment strategies</li>
      <li>Pacing and shaping of TCP and UDP traffic</li>
      <li>Parallel transfers and multistreaming</li>
      </ul>
  </li>
  <li>Performance evaluation
      <ul>
        <li>Modeling and simulation-based results</li>
        <li>Experiments on real networks and actual measurements</li>
        <li>Protocol benchmarking</li>
      </ul>
  </li>
  <li>Hardware-specific issues </li>
  <ul>
    <li>Transport over optical networks</li>
    <li>RDMA over WANs</li>
    <li>Protocol implementation and hardware issues (PCs, NICs, TOEs, routers, switches, etc.)</li>
    <li>Data replications and striping</li>
  </ul>
  <li>Application focus 
    <ul>
