  <h2>Paper Preparation</h2>
  <P>The camera-ready paper must be in PDF format for A4 or "US letter" sized paper.<br> 
  	Its length should typically be 4-5 pages, and up to a <em>maximum</em> length of <em>6</em> pages.<br> 
  	ALL fonts must be embedded in the PDF file.</p>
	<dl>
		<dt>IMPORTANT: </dt>
		<dd>Authors must check their final pdf files before submission to verify that all fonts have been properly embedded.<br>
		Some font manufacturers now flag their fonts not to embed and hence these fonts are unsuitable for submission.</dd>
		<dt>FILE NAME: </dt>
		<dd>Authors are to name their pdf submissions according to this format: &lt;full_lastName_firstNameInitial.pdf&gt;</dd>
		<dt>INSTRUCTIONS: </dt>
		<dd>To format your paper for the proceedings, please follow the 
		<a href="http://www.ieee.org/portal/site/mainsite/menuitem.818c0c39e85ef176fb2275875bac26c8/index.jsp?&pName=corp_level1&path=pubs/transactions&file=stylesheets.xml&xsl=generic.xsl" target="_blank">IEEE Transaction style</a> for 
	LaTeX and Word style files. <br>
		  A LaTeX example can be also
	    found <a href="IEEEsample2e.zip">here</a>.<br/>
	  The page numbers allocated to your paper are on the <a href="technical.php">programme page</a>. <strong>Please include the page numbers in your final paper.</strong>
	  </dd>
	</dl>
