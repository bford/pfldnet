<p>The 8th International Workshop on Protocols for Future, Large-Scale &amp; 
Diverse Network Transports (PFLDNeT) 
in beautiful <a href="http://www.downtownlancaster.com">Lancaster, Pennsylvania,</a>
will bring together researchers and 
practitioners from all over the world
working in the area of transport layer issues for 
modern communication networks.

 <p class="top">Previous PFLDNets held at 
CERN 
(<a href="http://datatag.web.cern.ch/datatag/pfldnet2003/" target="_blank">PFLDnet2003</a>),
  Argonne 
(<a href="http://www-didc.lbl.gov/PFLDnet2004/" target="_blank">PFLDnet2004</a>),
  Lyon 
	(<a href="http://www.ens-lyon.fr/LIP/RESO/pfldnet2005/" target="_blank">PFLDnet2005</a>),
  Nara 
	(<a href="http://www.hpcc.jp/pfldnet2006/" target="_blank">PFLDnet2006</a>),
ISI
	(<a href="http://wil.cs.caltech.edu/pfldnet2007/">PFLDnet2007</a>),
Manchester (PFLDnet2008),
and Tokyo (<a href="http://www.hpcc.jp/pfldnet2009/Top.html">PFLDNeT2009</a>)
  were very successful in bringing together researchers from all over the
	world, including North America, Europe and Asia.
This workshop will continue in this tradition of
providing researchers a focussed and highly interactive environment
to exchange their ideas and 
experiences in the area of transport layer problems.</p>

